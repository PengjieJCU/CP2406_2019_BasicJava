package week7;

public class Horse {
    String name;
    String color;
    int birthYear;

    public String getName() {
        return name;
    }

    public String getColor(){
        return color;
    }

    public int getBirthYear() {
        return birthYear;
    }

    public void setName(String fiName) {
        name = fiName;
    }

    public void setColor(String fiColor){
        color = fiColor;
    }

    public void setBirthYear(int fiBirthYear){
        birthYear = fiBirthYear;
    }
}